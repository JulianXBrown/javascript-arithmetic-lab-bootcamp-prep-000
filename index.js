const add = (a,b) => {
  return a + b;
};

const subtract = (a,b) => {
  return a - b;
};

const multiply = (a,b) => {
  return a * b;
};

const divide = (a,b) => {
  return a / b;
};

const inc = (n) => {
  return ++n;
};

const dec = (n) =>{
  return --n;
};

const makeInt = (n) => {
  return parseInt(n,10);
};

const perserveDecimal = (n) => {
  return parseFloat(n);
};